def make_window_plan(selected_species, n_windows=N_WINDOWS):
    n_sp   = len(selected_species)
    plan   = list(selected_species)
    extras = rng.choice(selected_species, size=n_windows - n_sp, replace=True).tolist()
    plan   = plan + extras
