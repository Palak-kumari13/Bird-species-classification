def download_audio(df, limit_per_species):
    AUDIO_EXTS = (".mp3", ".wav", ".ogg", ".flac", ".m4a")
    failed = []
    for code, grp in df.groupby("species_code"):
        en_name     = grp["species_en"].iloc[0]
        species_dir = os.path.join(AUDIO_DIR, code)
        os.makedirs(species_dir, exist_ok=True)
        subset = grp.head(limit_per_species)
        print(f"\n  [{code}] {en_name} — {len(subset)} files → audio/{code}/")
        for i, (_, row) in enumerate(subset.iterrows(), 1):
            rec_id    = row["id"]
            audio_url = str(row["file"] or "")
            fname     = row["file-name"] or f"XC{rec_id}.mp3"
            dest      = os.path.join(species_dir, fname)
            if os.path.exists(dest):
                print(f"    [{i}] skip: {fname}")
                continue
            if not audio_url:
                continue
            if audio_url.startswith("//"):
                audio_url = "https:" + audio_url
            try:
                r = requests.get(audio_url, timeout=60, stream=True)
                r.raise_for_status()
                with open(dest, "wb") as f:
                    for chunk in r.iter_content(8192):
                        f.write(chunk)
                print(f"    [{i}] {fname}  ({os.path.getsize(dest)//1024} KB)")
            except Exception as e:
                print(f"    [{i}] FAILED {fname}: {e}")
                failed.append(rec_id)
            time.sleep(0.3)
    if failed:
        print(f"\nFailed IDs: {failed}")
    return failed

