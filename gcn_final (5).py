            y, sr = librosa.load(src_path, sr=TARGET_SR, mono=True, dtype=np.float32)
            peak  = np.abs(y).max()
            if peak > 0:
                y = y / peak
            sf.write(dst_path, y, TARGET_SR,
                     subtype=TARGET_SUBTYPE, format=TARGET_FORMAT)
            manifest_rows.append({
                "file_path":    dst_path,
                "species_name": species_name,
                "class_id":     class_id,
            })
