def audio_to_mel(y: np.ndarray) -> np.ndarray:
    S    = librosa.feature.melspectrogram(
               y=y, sr=SR, n_fft=N_FFT, hop_length=HOP_LENGTH,
               n_mels=N_MELS, fmin=FMIN, fmax=FMAX, power=2.0)
    S_db = librosa.power_to_db(S, ref=np.max)
    mn, mx = S_db.min(), S_db.max()
    S_norm = (S_db - mn) / (mx - mn + 1e-8)
    img    = Image.fromarray((S_norm * 255).astype(np.uint8))
    img    = img.resize((MEL_W, MEL_H), Image.BILINEAR)
    S_r    = np.array(img, dtype=np.float32) / 255.0
    return np.stack([S_r, S_r, S_r], axis=0)

@torch.no_grad()
def run_windows(audio: np.ndarray) -> np.ndarray:
    mels  = [audio_to_mel(audio[i*CLIP_SAMPLES:(i+1)*CLIP_SAMPLES].astype(np.float32))
             for i in range(N_WINDOWS)]
    batch = torch.from_numpy(np.stack(mels, axis=0)).to(DEVICE)
    return model(batch).cpu().numpy()

def build_graph(probs: np.ndarray, top_k: int, label: np.ndarray) -> Data:
    vertices     = []
    edge_weights = {}
    mp           = []

    for i in range(N_WINDOWS):
        ranked = np.argsort(probs[i])[::-1]
        k_idx  = min(top_k - 1, len(ranked) - 1)
        sp     = int(ranked[k_idx])
        mp.append(sp)
        if sp not in vertices:
            vertices.append(sp)
        if i > 0:
            key = (mp[i-1], mp[i])
            edge_weights[key] = edge_weights.get(key, 0) + 1

    x_list = []
    for v in vertices:
        oh = np.zeros(NUM_CLASSES, dtype=np.float32)
        oh[v] = 1.0
        x_list.append(oh)
    x = torch.tensor(np.stack(x_list, axis=0), dtype=torch.float)

    vid_to_idx = {v: idx for idx, v in enumerate(vertices)}
    if edge_weights:
        edges      = list(edge_weights.items())
        edge_index = torch.tensor([[vid_to_idx[e[0][0]] for e in edges],
                                   [vid_to_idx[e[0][1]] for e in edges]], dtype=torch.long)
        edge_attr  = torch.tensor([[e[1]] for e in edges], dtype=torch.float)
    else:
        edge_index = torch.zeros((2, 0), dtype=torch.long)
        edge_attr  = torch.zeros((0, 1), dtype=torch.float)

    return Data(
        x=x, edge_index=edge_index, edge_attr=edge_attr,
        y=torch.tensor(label, dtype=torch.float),
        vertex_ids=torch.tensor(vertices, dtype=torch.long),
        num_nodes=len(vertices),
    )

