def evaluate(model, loader):
    model.eval()
    all_probs, all_labels, total_loss = [], [], 0.
    crit = nn.MSELoss()
    with torch.no_grad():
        for b in loader:
            b = b.to(DEVICE)
            p = model(b.x, b.edge_index, b.edge_attr, b.batch)
            total_loss += crit(p, b.y).item() * b.num_graphs
            all_probs.append(p.cpu().numpy())
            all_labels.append(b.y.cpu().numpy())
    probs_np  = np.vstack(all_probs)
    labels_np = np.vstack(all_labels).astype(int)
    avg_loss  = total_loss / len(loader.dataset)
    macro_f1  = f1_score(labels_np, (probs_np >= THRESHOLD).astype(int),
                         average="macro", zero_division=0)
    return avg_loss, macro_f1, probs_np, labels_np

def train_run(model, tr_loader, val_loader, optimiser, label, ckpt_path=None, verbose=True):
    crit    = nn.MSELoss()
    log     = []
    best_f1 = -1.; best_ep = -1; best_sd = None
    for epoch in range(1, MAX_EPOCHS + 1):
        model.train()
        tr_loss = 0.
        for b in tqdm(tr_loader, desc=f"{label} Ep{epoch:03d}", leave=False, unit="batch"):
            b = b.to(DEVICE)
            optimiser.zero_grad()
            loss = crit(model(b.x, b.edge_index, b.edge_attr, b.batch), b.y)
            loss.backward()
            optimiser.step()
            tr_loss += loss.item() * b.num_graphs
        tr_loss /= len(tr_loader.dataset)
        val_loss, val_f1, _, _ = evaluate(model, val_loader)
        is_best = val_f1 > best_f1
        if is_best:
            best_f1 = val_f1; best_ep = epoch
            best_sd = deepcopy(model.state_dict())
        log.append({"epoch": epoch, "train_loss": round(tr_loss, 6),
                    "val_loss": round(val_loss, 6), "val_f1": round(val_f1, 4),
                    "best": is_best})
        if verbose:
            print(f"  {label} Ep{epoch:03d}  tr={tr_loss:.5f}  val={val_loss:.5f}"
                  f"  f1={val_f1:.4f}" + ("  ✓" if is_best else ""))
    if ckpt_path and best_sd:
        torch.save({"epoch": best_ep, "state_dict": best_sd, "val_f1": best_f1}, ckpt_path)
    return pd.DataFrame(log), best_f1, best_ep, best_sd

