def parse_seconds(length_str):
    try:
        parts = str(length_str).strip().split(":")
        if len(parts) == 2:
            return int(parts[0]) * 60 + float(parts[1])
        if len(parts) == 3:
            return int(parts[0]) * 3600 + int(parts[1]) * 60 + float(parts[2])
    except Exception:
        pass
    return None

def fetch_species(genus, sp, en_name, code, max_pages=None):
    records  = []
    rejected = 0
    page     = 1
    while True:
        params = {"query": f"gen:{genus} sp:{sp}", "page": page, "key": API_KEY}
        resp = requests.get(BASE_URL, params=params, timeout=30)
        if not resp.ok:
            print(f"    HTTP {resp.status_code}: {resp.text[:150]}")
            break
        data      = resp.json()
        num_pages = int(data.get("numPages", 1))
        num_recs  = int(data.get("numRecordings", 0))
        recs      = data.get("recordings", [])
        valid, dropped = [], 0
        for r in recs:
            dur = parse_seconds(r.get("length", ""))
            if dur is not None and (dur < MIN_DUR or dur > MAX_DUR):
                dropped += 1
                continue
            r["_code"] = code
            r["_en"]   = en_name
            valid.append(r)
        records.extend(valid)
        rejected += dropped
        print(f"    page {page}/{num_pages}  fetched {len(recs)}  kept {len(valid)}  rejected {dropped}  (total available: {num_recs})")
        if page >= num_pages or (max_pages and page >= max_pages):
            break
        page += 1
        time.sleep(0.4)
    print(f"  total rejected [{code}]: {rejected}  valid: {len(records)}")
    return records

