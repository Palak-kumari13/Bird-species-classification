    src_dir = os.path.join(SRC_AUDIO_DIR, code)
    dst_dir = os.path.join(DST_RAW_DIR, species_name)
    os.makedirs(dst_dir, exist_ok=True)

    if not os.path.isdir(src_dir):
        print(f"[WARN] Source folder not found: {src_dir}")
        continue

    files = [f for f in os.listdir(src_dir)
             if f.lower().endswith((".mp3", ".wav", ".ogg", ".flac", ".m4a"))]
    print(f"\n[{code}] {species_name}  ({len(files)} files) → {dst_dir}")

    # ─── Resample all files ───────────────────────────────────────────────────
    for fname in tqdm(files, desc=code, unit="file"):
        src_path = os.path.join(src_dir, fname)
        stem     = os.path.splitext(fname)[0]
        dst_path = os.path.join(dst_dir, stem + ".wav")

        if os.path.exists(dst_path) and os.path.getsize(dst_path) > 0:
            manifest_rows.append({
                "file_path":    dst_path,
                "species_name": species_name,
                "class_id":     class_id,
            })
            continue

