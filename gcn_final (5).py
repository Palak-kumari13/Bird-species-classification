def audio_to_mel(y: np.ndarray) -> np.ndarray:
    S    = librosa.feature.melspectrogram(y=y, sr=SR, n_fft=N_FFT,
               hop_length=HOP_LENGTH, n_mels=N_MELS, fmin=FMIN, fmax=FMAX, power=2.)
    S_db = librosa.power_to_db(S, ref=np.max)
    mn, mx = S_db.min(), S_db.max()
    S_n    = (S_db - mn) / (mx - mn + 1e-8)
    img    = Image.fromarray((S_n * 255).astype(np.uint8)).resize((MEL_W, MEL_H), Image.BILINEAR)
    return np.stack([np.array(img, dtype=np.float32) / 255.] * 3, axis=0)

@torch.no_grad()
def run_windows(audio: np.ndarray) -> np.ndarray:
    mels  = [audio_to_mel(audio[i*CLIP_SAMPLES:(i+1)*CLIP_SAMPLES].astype(np.float32))
             for i in range(N_WINDOWS)]
    batch = torch.from_numpy(np.stack(mels)).to(DEVICE)
    return cnn_model(batch).cpu().numpy()

def build_graph(probs: np.ndarray, top_k: int, label: np.ndarray) -> Data:
    vertices = []; edge_weights = {}; mp = []
    for i in range(N_WINDOWS):
        sp = int(np.argsort(probs[i])[::-1][min(top_k - 1, NUM_CLASSES - 1)])
        mp.append(sp)
        if sp not in vertices: vertices.append(sp)
        if i > 0:
            key = (mp[i-1], mp[i])
            edge_weights[key] = edge_weights.get(key, 0) + 1
    x  = torch.tensor(np.eye(NUM_CLASSES, dtype=np.float32)[vertices], dtype=torch.float)
    vid = {v: idx for idx, v in enumerate(vertices)}
    if edge_weights:
        es = list(edge_weights.items())
        ei = torch.tensor([[vid[e[0][0]] for e in es], [vid[e[0][1]] for e in es]], dtype=torch.long)
        ea = torch.tensor([[e[1]] for e in es], dtype=torch.float)
    else:
        ei = torch.zeros((2, 0), dtype=torch.long)
        ea = torch.zeros((0, 1), dtype=torch.float)
    return Data(x=x, edge_index=ei, edge_attr=ea,
                y=torch.tensor(label, dtype=torch.float),
                vertex_ids=torch.tensor(vertices, dtype=torch.long),
                num_nodes=len(vertices))

