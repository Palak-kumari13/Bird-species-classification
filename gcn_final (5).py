class MelGCN(nn.Module):
    def __init__(self, in_ch=IN_DIM, hidden=HIDDEN_DIM, out=NUM_CLASSES, n_blocks=3):
        super().__init__()
        assert n_blocks in (1, 2, 3)
        self.n_blocks = n_blocks
        self.conv1 = GCNConv(in_ch, hidden, add_self_loops=True, normalize=True)
        if n_blocks >= 2:
            self.conv2 = GCNConv(hidden, hidden, add_self_loops=True, normalize=True)
        if n_blocks == 3:
            self.conv3 = GCNConv(hidden, out, add_self_loops=True, normalize=True)
        else:
            self.out_proj = nn.Linear(hidden, out)
        self.relu = nn.ReLU()
        self.sig  = nn.Sigmoid()

    def forward(self, x, edge_index, edge_attr, batch):
        x = self.relu(self.conv1(x, edge_index))
        if self.n_blocks >= 2:
            x = self.relu(self.conv2(x, edge_index))
        if self.n_blocks == 3:
            x = self.conv3(x, edge_index)
        else:
            x = self.out_proj(x)
        return self.sig(global_mean_pool(x, batch))

