class DoubleConvBlock(nn.Module):
    def __init__(self, in_ch, out_ch):
        super().__init__()
        self.block = nn.Sequential(
            nn.Conv2d(in_ch,  out_ch, 3, padding=1, bias=False),
            nn.BatchNorm2d(out_ch), nn.ReLU(inplace=True),
            nn.Conv2d(out_ch, out_ch, 3, padding=1, bias=False),
            nn.BatchNorm2d(out_ch), nn.ReLU(inplace=True),
            nn.MaxPool2d(2, 2),
        )
    def forward(self, x):
        return self.block(x)

class DeepCNN(nn.Module):
    def __init__(self, num_classes=NUM_CLASSES, dropout=0.5):
        super().__init__()
        self.features = nn.Sequential(
            DoubleConvBlock(  3,  32),
            DoubleConvBlock( 32,  64),
            DoubleConvBlock( 64, 128),
            DoubleConvBlock(128, 256),
        )
        with torch.no_grad():
            dummy    = torch.zeros(1, 3, 432, 1008)
            flat_dim = self.features(dummy).view(1, -1).shape[1]
        self.classifier = nn.Sequential(
            nn.Flatten(),
            nn.Linear(flat_dim, 512),
            nn.ReLU(inplace=True),
            nn.Dropout(dropout),
            nn.Linear(512, num_classes),
            nn.Sigmoid(),
        )
    def forward(self, x):
        return self.classifier(self.features(x))

class MelGCN(nn.Module):
    def __init__(self, in_ch=IN_DIM, hidden=HIDDEN_DIM, out=NUM_CLASSES, n_blocks=3):
        super().__init__()
        assert n_blocks in (1, 2, 3)
        self.n_blocks = n_blocks
        self.conv1 = GCNConv(in_ch, hidden, add_self_loops=True, normalize=True)
        if n_blocks >= 2:
            self.conv2 = GCNConv(hidden, hidden, add_self_loops=True, normalize=True)
        if n_blocks == 3:
            self.conv3 = GCNConv(hidden, out, add_self_loops=True, normalize=True)
        else:
            self.out_proj = nn.Linear(hidden, out)
        self.relu = nn.ReLU()
        self.sig  = nn.Sigmoid()
    def forward(self, x, edge_index, edge_attr, batch):
        x = self.relu(self.conv1(x, edge_index))
        if self.n_blocks >= 2:
            x = self.relu(self.conv2(x, edge_index))
        if self.n_blocks == 3:
            x = self.conv3(x, edge_index)
        else:
            x = self.out_proj(x)
        return self.sig(global_mean_pool(x, batch))

