# --- Internal helpers ---
_emb_store = {}
_cnn_hook  = _CNN.classifier[2].register_forward_hook(
    lambda m,i,o: _emb_store.update({"last": o.detach().cpu()})
)

def _to_mel(y):
    S    = librosa.feature.melspectrogram(y=y, sr=_SR, n_fft=_N_FFT,
               hop_length=_HOP, n_mels=_NMELS, fmin=_FMIN, fmax=_FMAX, power=2.)
    S_db = librosa.power_to_db(S, ref=np.max)
    mn,mx= S_db.min(), S_db.max()
    S_n  = (S_db-mn)/(mx-mn+1e-8)
    img  = Image.fromarray((S_n*255).astype(np.uint8)).resize((_MEL_W,_MEL_H),Image.BILINEAR)
    S_r  = np.array(img,dtype=np.float32)/255.
    return np.stack([S_r,S_r,S_r],axis=0)

@torch.no_grad()
def _cnn_windows(windows):
    batch  = torch.from_numpy(np.stack([_to_mel(w) for w in windows])).to(_DEVICE)
    probs  = _CNN(batch).cpu().numpy()
    embeds = _emb_store["last"].numpy()
    return probs, embeds

def _build_graph_inf(probs, embeds, top_k):
    vertices, v_emb, e_wts, mp = [], {}, {}, []
    for i in range(len(probs)):
        sp = int(np.argsort(probs[i])[::-1][min(top_k-1,_NCLS-1)])
        mp.append(sp)
        if sp not in vertices: vertices.append(sp); v_emb[sp]=[]
        v_emb[sp].append(embeds[i])
        if i > 0:
            k=(mp[i-1],mp[i]); e_wts[k]=e_wts.get(k,0)+1
    x  = torch.tensor(np.stack([np.mean(np.stack(v_emb[v]),0) for v in vertices]),
                      dtype=torch.float)
    vid= {v:i for i,v in enumerate(vertices)}
    if e_wts:
        es = list(e_wts.items())
        ei = torch.tensor([[vid[e[0][0]] for e in es],[vid[e[0][1]] for e in es]],dtype=torch.long)
        ea = torch.tensor([[e[1]] for e in es],dtype=torch.float)
    else:
        ei=torch.zeros((2,0),dtype=torch.long); ea=torch.zeros((0,1),dtype=torch.float)
    return (torch.zeros(_NCLS,dtype=torch.float),x,ei,ea,
            torch.tensor(vertices,dtype=torch.long))

@torch.no_grad()
def _gcn_predict(probs, embeds, n_graphs=2):
    sub_preds = []
    for k in range(1, n_graphs+1):
        y,x,ei,ea,vids = _build_graph_inf(probs, embeds, top_k=k)
        from torch_geometric.data import Data
        gd = Data(x=x, edge_index=ei, edge_attr=ea, y=y, num_nodes=x.shape[0])
        gb = Batch.from_data_list([gd]).to(_DEVICE)
        p  = _GCN(gb.x,gb.edge_index,gb.edge_attr,gb.batch).cpu().numpy().squeeze()
        sub_preds.append(p)
        n_nodes, n_edges = x.shape[0], ei.shape[1]
    final = np.stack(sub_preds).max(axis=0)
    return final, n_nodes, n_edges

# ═══════════════════════════════════════════════════════════════════════════════
def classify_recording(audio_path: str,
                       thresholds=None,
                       n_subgraphs: int = 2,
                       save_plot: bool = True,
                       output_json: str = "inference_output.json"):
    """
    Full end-to-end Mel Graph-GCN inference on a single audio file.

    Parameters
    ----------
    audio_path  : path to any audio file (mp3/wav/flac/ogg)
    thresholds  : per-class thresholds array (10,); defaults to tuned val thresholds
    n_subgraphs : number of top-k sub-graphs to build (2 for 2-species, 3 for 3-species)
    save_plot   : whether to save spectrogram+timeline overlay
    output_json : path to save JSON output

    Returns
    -------
    dict with keys: predicted_species, probabilities, timeline, graph_stats
    """
    t0 = time.time()
    if thresholds is None:
        thresholds = _THRESHOLDS

    # Step 1: Load + resample
    y, _ = librosa.load(audio_path, sr=_SR, mono=True, dtype=np.float32)

    # Step 2: Slice into 6 × 1.5s windows (pad last if needed)
    total_needed = _NWIN * _CLIP
    if len(y) < total_needed:
        y = np.pad(y, (0, total_needed - len(y)))
    y = y[:total_needed]
    windows = [y[i*_CLIP:(i+1)*_CLIP] for i in range(_NWIN)]

    # Step 3: Mel spectrogram for each window + CNN
    probs, embeds = _cnn_windows(windows)     # (6,10), (6,512)

    # Step 4+5+6: Build Mel Graphs + GCN inference
    final_probs, n_nodes, n_edges = _gcn_predict(probs, embeds, n_subgraphs)

    # Step 7: Per-class threshold
    preds = (final_probs >= thresholds).astype(int)

    # Step 8: Build return structure
    predicted_species = [_CLASS_NAMES[i] for i in range(_NCLS) if preds[i]==1]
    probabilities     = {_CLASS_NAMES[i]: float(round(final_probs[i],4))
                         for i in range(_NCLS)}
    timeline = []
    for i in range(_NWIN):
        top1 = int(np.argmax(probs[i]))
        top2 = int(np.argsort(probs[i])[-2])
        timeline.append({
            "window_idx":   i,
            "start_s":      round(i * 1.5, 1),
            "end_s":        round((i+1) * 1.5, 1),
            "top1_species": _CLASS_NAMES[top1],
            "top2_species": _CLASS_NAMES[top2],
            "probs_vector": [round(float(p),4) for p in probs[i]],
        })
    graph_stats = {
        "n_nodes":    n_nodes,
        "n_edges":    n_edges,
        "n_subgraphs":n_subgraphs,
        "degenerate": n_nodes <= 1,
    }
    result = {
        "audio_path":        audio_path,
        "predicted_species": predicted_species,
        "probabilities":     probabilities,
        "timeline":          timeline,
        "graph_stats":       graph_stats,
        "inference_time_s":  round(time.time()-t0, 3),
    }

    # Save JSON
    with open(output_json, "w") as f:
        json.dump(result, f, indent=2)

    # Step 9: Spectrogram + timeline plot
    if save_plot:
        S_full = librosa.feature.melspectrogram(y=y, sr=_SR, n_fft=_N_FFT,
                     hop_length=_HOP, n_mels=_NMELS, fmin=_FMIN, fmax=_FMAX, power=2.)
        S_db   = librosa.power_to_db(S_full, ref=np.max)

        import librosa.display
        fig, (ax_s, ax_t) = plt.subplots(2,1,figsize=(13,5),
                                          gridspec_kw={"height_ratios":[3,1]})
        librosa.display.specshow(S_db,sr=_SR,hop_length=_HOP,
                                 x_axis="time",y_axis="mel",ax=ax_s,
                                 fmin=_FMIN,fmax=_FMAX,cmap="magma")
        ax_s.set_title(
            f"Mel Graph-GCN Prediction: {', '.join(predicted_species) or 'None'}",
            fontsize=11,fontweight="bold"
        )
        for i in range(1,_NWIN):
            ax_s.axvline(i*1.5,color="white",lw=0.8,ls="--",alpha=0.5)

        ax_t.set_xlim(0,9); ax_t.set_ylim(-0.5,1.5)
        ax_t.set_yticks([0,1]); ax_t.set_yticklabels(["Top-2","Top-1"],fontsize=9)
        ax_t.set_xlabel("Time (s)")
        legend_patches={}
        for i,entry in enumerate(timeline):
            for row_y, sp_name in [(1,entry["top1_species"]),(0,entry["top2_species"])]:
                sp_idx = _CLASS_NAMES.index(sp_name)
                col    = _COLORS(sp_idx)
                ax_t.barh(row_y,1.5,left=i*1.5,height=0.4,color=col,
                          edgecolor="white",lw=0.5)
                ax_t.text(i*1.5+0.75,row_y,sp_name[:7],ha="center",va="center",
                          fontsize=6,color="white",fontweight="bold")
                legend_patches[sp_idx]=mpatches.Patch(color=col,label=sp_name)
        ax_t.legend(handles=list(legend_patches.values()),
                    loc="upper right",fontsize=7,ncol=3)
        ax_t.grid(axis="x",alpha=0.3)
        plt.tight_layout()
        plot_path = output_json.replace(".json","_plot.png")
        plt.savefig(plot_path,dpi=120,bbox_inches="tight")
        plt.show()
        print(f"  Plot saved → {plot_path}")

    # Print summary
    print(f"\n  Audio          : {audio_path}")
    print(f"  Predicted      : {predicted_species}")
    print(f"  Graph stats    : {n_nodes} nodes, {n_edges} edges"
          + (" [DEGENERATE]" if graph_stats["degenerate"] else ""))
    print(f"  Inference time : {result['inference_time_s']} s")
    print(f"  Output JSON    → {output_json}")

    return result

