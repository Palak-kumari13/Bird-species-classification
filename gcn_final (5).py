def make_window_plan(selected, n_windows=N_WINDOWS):
    plan   = list(selected)
    extras = rng.choice(selected, size=n_windows - len(selected), replace=True).tolist()
    plan   = plan + extras
    rng.shuffle(plan)
    return plan

def load_clip(path):
    c = np.load(path).astype(np.float32)
    if len(c) < CLIP_SAMPLES:
        c = np.pad(c, (0, CLIP_SAMPLES - len(c)))
    return c[:CLIP_SAMPLES]

def build_composite(window_plan):
    used = {sp: [] for sp in set(window_plan)}
    windows = []
    for cid in window_plan:
        pool  = clip_pool[cid]
        avail = [p for p in pool if p not in used[cid]] or pool
        chosen = rng.choice(avail)
        used[cid].append(chosen)
        windows.append(load_clip(chosen))
    comp = np.concatenate(windows)
    peak = np.abs(comp).max()
    if peak > 1e-8:
        comp = comp * (PEAK_NORM / peak)
    return comp

