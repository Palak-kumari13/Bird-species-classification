def segment_recording(wav_path: str, species_code: str) -> list[dict]:
    """
    Slice a recording into SEG_SAMPLES-long segments.
    - Recordings shorter than SEG_SAMPLES: zero-padded to exactly SEG_SAMPLES.
    - Recordings longer than SEG_SAMPLES: sliding window with HOP_SAMPLES step.
      Final partial window is zero-padded if it contains at least 1 sec of audio.
    Returns a list of metadata dicts for each segment saved.
    """
    y, sr = sf.read(wav_path, dtype="float32")
    assert sr == TARGET_SR, f"Expected {TARGET_SR} Hz, got {sr}"
    if y.ndim > 1:
        y = y.mean(axis=1)   # safety: mono

    class_id     = code_to_id[species_code]
    species_name = code_to_name[species_code]
    label_vec    = one_hot([class_id])

    out_dir = os.path.join(GRAPH_DIR, species_name)
    os.makedirs(out_dir, exist_ok=True)

    n_samples = len(y)
    segments  = []

    if n_samples <= SEG_SAMPLES:
        # Short recording → single zero-padded segment
        padded  = np.zeros(SEG_SAMPLES, dtype=np.float32)
        padded[:n_samples] = y
        segments.append(padded)
    else:
        # Sliding window
        start = 0
        while start < n_samples:
            end   = start + SEG_SAMPLES
            chunk = y[start:end]
            if len(chunk) < TARGET_SR:          # < 1 sec of real audio → discard
                break
            if len(chunk) < SEG_SAMPLES:        # partial last window → zero-pad
                padded       = np.zeros(SEG_SAMPLES, dtype=np.float32)
                padded[:len(chunk)] = chunk
                chunk        = padded
            segments.append(chunk.astype(np.float32))
            start += HOP_SAMPLES

    # Write segments
    existing  = len(list(Path(out_dir).glob("seg_*.wav")))
    seg_metas = []
    for i, seg in enumerate(segments):
        seg_idx  = existing + i + 1
        seg_name = f"seg_{seg_idx:05d}.wav"
        seg_path = os.path.join(out_dir, seg_name)
        sf.write(seg_path, seg, TARGET_SR, subtype="FLOAT")
        seg_metas.append({
            "filepath":        seg_path,
            "filename":        seg_name,
            "species_code":    species_code,
            "species_name":    species_name,
            "class_id":        class_id,
            "label_onehot":    label_vec,
            "seg_index":       i,
            "n_segments":      len(segments),
            "duration_sec":    round(SEG_SAMPLES / TARGET_SR, 4),
            "padded":          int(len(y) < SEG_SAMPLES or (len(y) % SEG_SAMPLES != 0 and i == len(segments)-1)),
            "source_wav":      wav_path,
        })
    return seg_metas

