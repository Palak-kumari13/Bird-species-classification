def wav_to_log_mel(wav_path: str) -> np.ndarray:
    """
    Load WAV → compute log mel spectrogram.
    Output shape: (N_MELS, time_frames) = (64, ~150) for 1.5-sec clips.
    Values are in dB, normalized to [0, 1] range via TOP_DB clipping.
    """
    y, sr = sf.read(wav_path, dtype="float32")
    assert sr == SR, f"Expected {SR} Hz, got {sr} in {wav_path}"

    # Power spectrogram
    stft  = librosa.stft(y, n_fft=N_FFT, hop_length=HOP_LENGTH, center=True)
    power = np.abs(stft) ** 2

    # Mel filterbank
    mel   = np.dot(mel_filterbank, power)

    # Log mel (dB) with reference to max
    log_mel = librosa.power_to_db(mel, ref=np.max, top_db=TOP_DB)

    # Normalize to [0, 1]
    log_mel = (log_mel + TOP_DB) / TOP_DB

    return log_mel.astype(np.float32)

