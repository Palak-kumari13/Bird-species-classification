# ─── Processing function ──────────────────────────────────────────────────────
def process_audio(src_path, dst_path, target_sr=TARGET_SR):
    """
    Pipeline:
      1. Load  (any SR, any channels)
      2. Stereo → Mono
      3. Trim leading/trailing silence
      4. Peak normalize  → max |amplitude| = 1.0
      5. Resample → 32 kHz
      6. Save as WAV (float32)
    Returns dict of stats or raises on failure.
    """
    # 1. Load
    y, sr = librosa.load(src_path, sr=None, mono=False)
    orig_sr       = sr
    orig_channels = 1 if y.ndim == 1 else y.shape[0]
    orig_duration = y.shape[-1] / sr

    # 2. Stereo → Mono
    if y.ndim > 1:
        y = librosa.to_mono(y)

    # 3. Trim silence
    y_trimmed, _ = librosa.effects.trim(
        y,
        top_db=TOP_DB,
        frame_length=FRAME_LEN,
        hop_length=HOP_LEN
    )
    # Guard: if trim removes everything, fall back to original
    if len(y_trimmed) < sr * 0.1:   # < 0.1 sec after trim → skip trim
        y_trimmed = y
    trim_duration = len(y_trimmed) / sr

    # 4. Peak normalize
    peak = np.max(np.abs(y_trimmed))
    if peak > 0:
        y_norm = y_trimmed / peak
    else:
        y_norm = y_trimmed

    # 5. Resample to 32 kHz
    if sr != target_sr:
        y_resampled = librosa.resample(y_norm, orig_sr=sr, target_sr=target_sr)
    else:
        y_resampled = y_norm
    final_duration = len(y_resampled) / target_sr

    # 6. Save as WAV float32
    os.makedirs(os.path.dirname(dst_path), exist_ok=True)
    sf.write(dst_path, y_resampled.astype(np.float32), target_sr, subtype="FLOAT")

    return {
        "orig_sr":        orig_sr,
        "orig_channels":  orig_channels,
        "orig_duration":  round(orig_duration, 3),
        "trim_duration":  round(trim_duration, 3),
        "final_duration": round(final_duration, 3),
        "final_sr":       target_sr,
        "file_size_kb":   round(os.path.getsize(dst_path) / 1024, 1),
    }

