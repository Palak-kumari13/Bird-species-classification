# 3.  TIMELINE METRICS  (Section 13)
# ═══════════════════════════════════════════════════════════════════════════════

def smooth_probs(probs: np.ndarray, window: int = SMOOTH_WIN) -> np.ndarray:
    """
    Apply per-class moving-average smoothing over the time axis.

    Parameters
    ----------
    probs  : (T, C) probability matrix over windows
    window : smoothing window size

    Returns
    -------
    smoothed : (T, C) smoothed probabilities
    """
    kernel   = np.ones(window) / window
    smoothed = np.stack(
        [np.convolve(probs[:, c], kernel, mode="same") for c in range(probs.shape[1])],
        axis=1,
    )
    return smoothed.astype(np.float32)


def build_timeline(
    probs: np.ndarray,
    thresholds: np.ndarray,
    stride_sec: float = STRIDE_HALF / TARGET_SR,
    min_dur: float = MIN_SEG_DUR,
) -> list[dict]:
    """
    Convert per-window probabilities into a temporal activity timeline.

    Algorithm:
      1. Smooth probabilities with moving average.
      2. Threshold per class to get binary presence per window.
      3. Merge consecutive active windows per class into segments.
      4. Discard segments shorter than min_dur.
      5. Record start, end, and mean confidence for each segment.

    Parameters
    ----------
    probs      : (T, C) float — sigmoid probabilities per window
    thresholds : (C,) float  — per-class thresholds
    stride_sec : time advance per window in seconds
    min_dur    : minimum segment duration to keep (seconds)

    Returns
    -------
    timeline : list of dicts with keys bird, start, end, confidence
    """
    smoothed = smooth_probs(probs)
    T, C     = smoothed.shape
    timeline = []

    for c in range(C):
        active    = smoothed[:, c] >= thresholds[c]
        in_seg    = False
        seg_start = 0

        for t in range(T):
            if active[t] and not in_seg:
                in_seg    = True
                seg_start = t
            elif not active[t] and in_seg:
                in_seg = False
                dur    = (t - seg_start) * stride_sec
                if dur >= min_dur:
                    confidence = float(smoothed[seg_start:t, c].mean())
                    timeline.append({
                        "bird":       CLASS_NAMES[c],
                        "start":      round(seg_start * stride_sec, 3),
                        "end":        round(t * stride_sec, 3),
                        "confidence": round(confidence, 4),
                    })

        # Close any open segment at end of recording
        if in_seg:
            dur = (T - seg_start) * stride_sec
            if dur >= min_dur:
                confidence = float(smoothed[seg_start:T, c].mean())
                timeline.append({
                    "bird":       CLASS_NAMES[c],
                    "start":      round(seg_start * stride_sec, 3),
                    "end":        round(T * stride_sec, 3),
                    "confidence": round(confidence, 4),
                })

    timeline.sort(key=lambda x: x["start"])
    return timeline


def temporal_iou(pred_seg: tuple, gt_seg: tuple) -> float:
    """
    Compute temporal Intersection over Union between two segments.

    Parameters
    ----------
    pred_seg, gt_seg : (start, end) tuples in seconds

    Returns
    -------
    iou : float in [0, 1]
    """
    ps, pe = pred_seg
    gs, ge = gt_seg
    inter  = max(0.0, min(pe, ge) - max(ps, gs))
    union  = max(pe, ge) - min(ps, gs)
    return inter / union if union > 0 else 0.0


def timeline_metrics(
    pred_timeline: list[dict],
    gt_timeline: list[dict],
    iou_threshold: float = 0.5,
) -> dict:
    """
    Compute timeline-level detection metrics.

    Metrics
    -------
    - Segment-based F1  : treats each predicted segment as a detection event
    - Mean temporal IoU : average IoU between matched pred/GT segment pairs
    - Onset accuracy    : fraction of GT onsets within 0.5 sec of a prediction
    - Offset accuracy   : fraction of GT offsets within 0.5 sec of a prediction

    Parameters
    ----------
    pred_timeline : list of dicts from build_timeline()
    gt_timeline   : list of dicts (same format) — ground-truth annotations
    iou_threshold : IoU threshold for a successful match

    Returns
    -------
    dict of timeline metric values
    """
    tp = fp = fn = 0
    ious       = []
    onset_hits = []
    offset_hits = []

    matched_gt = set()

    for pred in pred_timeline:
        pc = pred["bird"]
        ps, pe = pred["start"], pred["end"]
        found  = False

        for gi, gt in enumerate(gt_timeline):
            if gi in matched_gt or gt["bird"] != pc:
                continue
            gs, ge  = gt["start"], gt["end"]
            iou_val = temporal_iou((ps, pe), (gs, ge))
            if iou_val >= iou_threshold:
                tp  += 1
                ious.append(iou_val)
                onset_hits.append(abs(ps - gs) <= 0.5)
                offset_hits.append(abs(pe - ge) <= 0.5)
                matched_gt.add(gi)
                found = True
                break

        if not found:
            fp += 1

    fn = len(gt_timeline) - len(matched_gt)

    precision     = tp / (tp + fp + 1e-9)
    recall        = tp / (tp + fn + 1e-9)
    seg_f1        = 2 * precision * recall / (precision + recall + 1e-9)
    mean_iou      = float(np.mean(ious))    if ious        else 0.0
    onset_acc     = float(np.mean(onset_hits))  if onset_hits  else 0.0
    offset_acc    = float(np.mean(offset_hits)) if offset_hits else 0.0

    results = {
        "segment_f1":    round(seg_f1,     4),
        "mean_iou":      round(mean_iou,   4),
        "onset_acc":     round(onset_acc,  4),
        "offset_acc":    round(offset_acc, 4),
        "tp": tp, "fp": fp, "fn": fn,
    }

    print("\n  Timeline Detection Metrics:")
    print(f"    Segment-based F1  : {seg_f1:.4f}")
    print(f"    Mean temporal IoU : {mean_iou:.4f}")
    print(f"    Onset accuracy    : {onset_acc:.4f}  (within 0.5 sec)")
    print(f"    Offset accuracy   : {offset_acc:.4f}  (within 0.5 sec)")
    print(f"    TP={tp}  FP={fp}  FN={fn}")

    return results


