def one_hot(class_id: int, num_classes: int = NUM_CLASSES) -> list[int]:
    vec = [0] * num_classes
    vec[class_id] = 1
    return vec

def split_into_clips(wav_path: str, species_code: str) -> list[dict]:
    """
    Load a WAV and yield non-overlapping 1.5-sec clips.
    Clips shorter than CLIP_SAMPLES at the end are discarded.
    Returns a list of dicts describing each clip written.
    """
    y, sr = sf.read(wav_path, dtype="float32")
    assert sr == TARGET_SR, f"Expected {TARGET_SR} Hz, got {sr} Hz in {wav_path}"
    assert y.ndim == 1,     f"Expected mono audio, got shape {y.shape} in {wav_path}"

    class_id   = code_to_id[species_code]
    label_vec  = one_hot(class_id)
    species_name = code_to_name[species_code]

    # Output folder: isolated_clips/<species_name>/
    out_dir = os.path.join(CLIPS_DIR, species_name)
    os.makedirs(out_dir, exist_ok=True)

    n_clips    = len(y) // CLIP_SAMPLES          # integer division → discard remainder
    clip_metas = []

    for i in range(n_clips):
        start  = i * CLIP_SAMPLES
        end    = start + CLIP_SAMPLES
        chunk  = y[start:end]

        assert len(chunk) == CLIP_SAMPLES, "Clip length mismatch — should never happen"

        # Count existing clips in folder to keep numbering global per species
        clip_idx  = len(list(Path(out_dir).glob("clip_*.wav"))) + 1
        clip_name = f"clip_{clip_idx:04d}.wav"
        clip_path = os.path.join(out_dir, clip_name)

        sf.write(clip_path, chunk, TARGET_SR, subtype="FLOAT")

        clip_metas.append({
            "clip_path":     clip_path,
            "clip_filename": clip_name,
            "species_code":  species_code,
            "species_name":  species_name,
            "class_id":      class_id,
            "label_onehot":  label_vec,
            "clip_index":    i,
            "n_samples":     len(chunk),
            "duration_sec":  round(len(chunk) / TARGET_SR, 4),
            "source_wav":    wav_path,
        })

    return clip_metas

