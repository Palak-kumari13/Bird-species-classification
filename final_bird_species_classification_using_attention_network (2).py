def run_full_evaluation(
    y_true_val:  np.ndarray,
    y_prob_val:  np.ndarray,
    y_true_test: np.ndarray,
    y_prob_test: np.ndarray,
    test_wav_path: str | None = None,
    window_probs_demo: np.ndarray | None = None,
    gt_timeline_demo:  list[dict] | None = None,
):
    """
    Run the complete evaluation pipeline:
      1. Tune per-class thresholds on validation set.
      2. Compute all core metrics on test set.
      3. Plot training curves, confusion matrix, ROC curves.
      4. (Optional) plot spectrogram + predictions, activity heatmap.
      5. (Optional) compute timeline metrics.

    Parameters
    ----------
    y_true_val / y_prob_val   : validation ground-truth and probabilities
    y_true_test / y_prob_test : test ground-truth and probabilities
    test_wav_path             : path to a test WAV for spectrogram plot
    window_probs_demo         : (T, C) probs for timeline / heatmap demo
    gt_timeline_demo          : ground-truth timeline for timeline metrics
    """
    # 1. Threshold tuning on validation
    opt_thresholds = tune_thresholds(y_true_val, y_prob_val)

    # 2. Core metrics with fixed and tuned thresholds
    print("\n── Fixed threshold (0.5) ─────────────────────────────")
    compute_core_metrics(y_true_test, y_prob_test, tag="test_fixed_thr")

    print("\n── Tuned per-class thresholds ────────────────────────")
    compute_core_metrics(y_true_test, y_prob_test,
                         thresholds=opt_thresholds, tag="test_tuned_thr")

    # 3. Training curves
    print("\n── Plotting training curves ──────────────────────────")
    plot_training_curves()

    # 4. Confusion matrix
    print("\n── Plotting confusion matrices ───────────────────────")
    y_pred_test = (y_prob_test >= opt_thresholds).astype(int)
    plot_confusion_matrix(y_true_test, y_pred_test, tag="test")

    # 5. ROC curves
    print("\n── Plotting ROC curves ───────────────────────────────")
    plot_roc_curves(y_true_test, y_prob_test, tag="test")

    # 6. Optional spectrogram + heatmap
