# ─── Fallback MLP ─────────────────────────────────────────────────────────────
class FallbackMLP(nn.Module):
    """
    2-layer MLP on averaged node features.
    Used when the graph has no edges or only one node.
    Input : (B, node_dim)   Output : (B, num_classes) logits
    """
    def __init__(self, in_dim=NODE_DIM, hidden=64,
                 num_classes=NUM_CLASSES, dropout=DROPOUT):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(in_dim, hidden),
            nn.BatchNorm1d(hidden),
            nn.ReLU(),
            nn.Dropout(dropout),
            nn.Linear(hidden, num_classes),
        )
        for m in self.modules():
            if isinstance(m, nn.Linear):
                nn.init.xavier_uniform_(m.weight)
                nn.init.zeros_(m.bias)

    def forward(self, x):
        return self.net(x)


# ─── GAT Model ────────────────────────────────────────────────────────────────
class GATClassifier(nn.Module):
    """
    3-layer GAT with three-tier fallback:
      Tier 1 — Isolated nodes: repaired with self-loops at build time → GAT runs.
      Tier 2 — data.degenerate == True (0 edges / 1 node) → FallbackMLP.
      Tier 3 — Unexpected runtime error in GAT → silent FallbackMLP + warning.
    """
    def __init__(self, in_dim=NODE_DIM, hidden=HIDDEN, heads1=HEADS_1,
                 heads2=HEADS_2, num_classes=NUM_CLASSES, dropout=DROPOUT):
        super().__init__()
        self.drop = dropout

        self.conv1 = GATConv(in_dim,         hidden, heads=heads1, dropout=dropout, concat=True)
        self.conv2 = GATConv(hidden * heads1, hidden, heads=heads1, dropout=dropout, concat=True)
        self.conv3 = GATConv(hidden * heads1, hidden, heads=heads2, dropout=dropout, concat=False)

        self.bn1 = nn.BatchNorm1d(hidden * heads1)
        self.bn2 = nn.BatchNorm1d(hidden * heads1)
        self.bn3 = nn.BatchNorm1d(hidden)
        self.fc  = nn.Linear(hidden, num_classes)

        self.fallback = FallbackMLP(in_dim=in_dim, hidden=hidden,
                                    num_classes=num_classes, dropout=dropout)
        nn.init.xavier_uniform_(self.fc.weight)
        nn.init.zeros_(self.fc.bias)

    def _gat_forward(self, x, edge_index, batch):
        x = self.conv1(x, edge_index);  x = self.bn1(x);  x = F.elu(x)
        x = F.dropout(x, p=self.drop, training=self.training)
        x = self.conv2(x, edge_index);  x = self.bn2(x);  x = F.elu(x)
        x = F.dropout(x, p=self.drop, training=self.training)
        x = self.conv3(x, edge_index);  x = self.bn3(x);  x = F.elu(x)
        x = global_mean_pool(x, batch)
        return self.fc(x)

    def _fallback_forward(self, x, batch):
        return self.fallback(global_mean_pool(x, batch))

    def _safe_gat(self, x, edge_index, batch):
        try:
            return self._gat_forward(x, edge_index, batch)
        except Exception as exc:
            warnings.warn(
                f"[GATClassifier] GAT forward failed ({type(exc).__name__}: {exc}). "
                "Using FallbackMLP.", RuntimeWarning, stacklevel=2)
            return self._fallback_forward(x, batch)

    def forward(self, x, edge_index, edge_weight, batch, degenerate_mask=None):
        B = int(batch.max().item()) + 1 if batch.numel() > 0 else 1

        if degenerate_mask is None:
            if edge_index.shape[1] == 0:
                degenerate_mask = torch.ones(B, dtype=torch.bool, device=x.device)
            else:
                has_edge = torch.zeros(B, dtype=torch.bool, device=x.device)
                src_graphs = batch[edge_index[0]]
                has_edge.scatter_(0, src_graphs,
                                  torch.ones_like(src_graphs, dtype=torch.bool))
                degenerate_mask = ~has_edge

        if (~degenerate_mask).all():
            return self._safe_gat(x, edge_index, batch)
        if degenerate_mask.all():
            return self._fallback_forward(x, batch)

        # Mixed batch — route per graph
        logits = torch.zeros(B, self.fc.out_features, device=x.device)
        for g in range(B):
            node_mask = (batch == g)
            x_g       = x[node_mask]
            batch_g   = torch.zeros(node_mask.sum(), dtype=torch.long, device=x.device)
            if degenerate_mask[g]:
                logits[g] = self._fallback_forward(x_g, batch_g).squeeze(0)
            else:
                edge_mask = node_mask[edge_index[0]] & node_mask[edge_index[1]]
                ei_g      = edge_index[:, edge_mask]
                node_idx  = torch.where(node_mask)[0]
                idx_map   = torch.zeros(x.shape[0], dtype=torch.long, device=x.device)
                idx_map[node_idx] = torch.arange(node_mask.sum(), device=x.device)
                logits[g] = self._safe_gat(x_g, idx_map[ei_g], batch_g).squeeze(0)
        return logits


# ─── Dataset ──────────────────────────────────────────────────────────────────
class GraphFileDataset(Dataset):
    def __init__(self, df):
        super().__init__()
        self.paths  = df["graph_path"].tolist()
        self.labels = []
        for lbl in df["label_onehot"]:
            if isinstance(lbl, str):
                lbl = ast.literal_eval(lbl)
            self.labels.append(torch.tensor(lbl, dtype=torch.float32))

    def len(self):
        return len(self.paths)

    def get(self, idx):
        g = torch.load(self.paths[idx], weights_only=False)
        g.y = self.labels[idx].unsqueeze(0)
        if not hasattr(g, "degenerate"):
            g.degenerate = (g.edge_index.numel() == 0) or (g.num_nodes <= 1)
        return g


# ─── Helpers ──────────────────────────────────────────────────────────────────
def macro_f1(logits, targets, threshold=0.5):
    probs = torch.sigmoid(logits).cpu().numpy()
    preds = (probs >= threshold).astype(int)
    return f1_score(targets.cpu().numpy(), preds, average="macro", zero_division=0)


def get_degenerate_mask(batch):
    B = int(batch.batch.max().item()) + 1
    if hasattr(batch, "degenerate"):
        degen = batch.degenerate
        if isinstance(degen, torch.Tensor):
            return degen.view(B).bool().to(batch.x.device)
        return torch.full((B,), bool(degen), dtype=torch.bool, device=batch.x.device)
    has_edge = torch.zeros(B, dtype=torch.bool, device=batch.x.device)
    if batch.edge_index.shape[1] > 0:
        src_graphs = batch.batch[batch.edge_index[0]]
        has_edge.scatter_(0, src_graphs,
                          torch.ones(src_graphs.shape[0], dtype=torch.bool,
                                     device=batch.x.device))
    return ~has_edge


def print_per_species_table(labels_np, preds_np, class_names):
    """
    Print a per-species Precision / Recall / F1 table (like paper Table 2),
    followed by macro-average row.

    Parameters
    ----------
    labels_np   : np.ndarray  shape (N, C)  — ground-truth binary
    preds_np    : np.ndarray  shape (N, C)  — predicted binary
    class_names : list[str]   length C      — species names ordered by class index
    """
    per_p  = precision_score(labels_np, preds_np, average=None, zero_division=0)
    per_r  = recall_score(   labels_np, preds_np, average=None, zero_division=0)
    per_f1 = f1_score(       labels_np, preds_np, average=None, zero_division=0)

    macro_p  = precision_score(labels_np, preds_np, average="macro", zero_division=0)
    macro_r  = recall_score(   labels_np, preds_np, average="macro", zero_division=0)
    macro_f  = f1_score(       labels_np, preds_np, average="macro", zero_division=0)

    # Column widths
    name_w = max(len(n) for n in class_names + ["Macro Average"]) + 2
    col_w  = 11

    sep   = "+" + "-" * (name_w + 2) + "+" + ("-" * (col_w + 2) + "+") * 3
    header = (f"| {'Species':<{name_w}} | {'Precision':^{col_w}} |"
              f" {'Recall':^{col_w}} | {'F1-Score':^{col_w}} |")

    print(sep)
    print(header)
    print(sep)
    for i, name in enumerate(class_names):
        print(f"| {name:<{name_w}} | {per_p[i]:^{col_w}.4f} |"
              f" {per_r[i]:^{col_w}.4f} | {per_f1[i]:^{col_w}.4f} |")
    print(sep)
    print(f"| {'Macro Average':<{name_w}} | {macro_p:^{col_w}.4f} |"
          f" {macro_r:^{col_w}.4f} | {macro_f:^{col_w}.4f} |")
    print(sep)


