def compute_core_metrics(
    y_true: np.ndarray,
    y_prob: np.ndarray,
    thresholds: np.ndarray | None = None,
    tag: str = "test",
) -> dict:
    """
    Compute the full set of required multi-label classification metrics.

    Parameters
    ----------
    y_true      : (N, C) int array  — ground-truth multi-hot labels
    y_prob      : (N, C) float array — sigmoid probabilities
    thresholds  : (C,) float array  — per-class thresholds; uses THRESHOLD if None
    tag         : label for printing / saving

    Returns
    -------
    dict with all metric values
    """
    if thresholds is None:
        thresholds = np.full(NUM_CLASSES, THRESHOLD)

    y_pred = (y_prob >= thresholds).astype(int)

    macro_f1  = f1_score(y_true, y_pred, average="macro",   zero_division=0)
    micro_f1  = f1_score(y_true, y_pred, average="micro",   zero_division=0)
    macro_p   = precision_score(y_true, y_pred, average="macro",  zero_division=0)
    micro_p   = precision_score(y_true, y_pred, average="micro",  zero_division=0)
    macro_r   = recall_score(y_true, y_pred, average="macro",  zero_division=0)
    micro_r   = recall_score(y_true, y_pred, average="micro",  zero_division=0)

    # ROC-AUC (macro)
    try:
        roc_auc_macro = roc_auc_score(y_true, y_prob, average="macro")
        roc_auc_micro = roc_auc_score(y_true, y_prob, average="micro")
    except ValueError:
        roc_auc_macro = roc_auc_micro = float("nan")

    # Per-class F1 and accuracy
    per_class_f1  = f1_score(y_true, y_pred, average=None, zero_division=0)
    # Per-class accuracy: fraction of samples correctly predicted for each class
    per_class_acc = (y_pred == y_true).mean(axis=0)

    results = {
        "tag":             tag,
        "macro_f1":        round(float(macro_f1),       4),
        "micro_f1":        round(float(micro_f1),       4),
        "macro_precision": round(float(macro_p),        4),
        "micro_precision": round(float(micro_p),        4),
        "macro_recall":    round(float(macro_r),        4),
        "micro_recall":    round(float(micro_r),        4),
        "roc_auc_macro":   round(float(roc_auc_macro),  4),
        "roc_auc_micro":   round(float(roc_auc_micro),  4),
        "per_class_f1":    {CLASS_NAMES[i]: round(float(v), 4) for i, v in enumerate(per_class_f1)},
        "per_class_acc":   {CLASS_NAMES[i]: round(float(v), 4) for i, v in enumerate(per_class_acc)},
    }

    # ── Print ────────────────────────────────────────────────────────────────
    print(f"\n{'='*60}")
    print(f"EVALUATION RESULTS  [{tag.upper()}]")
    print("=" * 60)
    print(f"  Macro  F1        : {macro_f1:.4f}")
    print(f"  Micro  F1        : {micro_f1:.4f}")
    print(f"  Macro  Precision : {macro_p:.4f}")
    print(f"  Micro  Precision : {micro_p:.4f}")
    print(f"  Macro  Recall    : {macro_r:.4f}")
    print(f"  Micro  Recall    : {micro_r:.4f}")
    print(f"  ROC-AUC (macro)  : {roc_auc_macro:.4f}")
    print(f"  ROC-AUC (micro)  : {roc_auc_micro:.4f}")
    print(f"\n  Per-class F1:")
    for name, val in results["per_class_f1"].items():
        bar = "█" * int(val * 20)
        print(f"    {name:<25} {val:.4f}  {bar}")
    print(f"\n  Per-class Accuracy:")
    for name, val in results["per_class_acc"].items():
        print(f"    {name:<25} {val:.4f}")

    # Save JSON
    out_json = os.path.join(OUT_DIR, f"metrics_{tag}.json")
    with open(out_json, "w") as f:
        json.dump(results, f, indent=2)
    print(f"\n  Saved → {out_json}")

    return results


