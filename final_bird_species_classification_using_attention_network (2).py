def cosine_sim(a, b):
    denom = np.linalg.norm(a) * np.linalg.norm(b)
    return float(np.dot(a, b) / denom) if denom > 1e-9 else 0.0

# ─── Graph validation ─────────────────────────────────────────────────────────
def validate_graph(data):
    N = data.num_nodes
    E = data.edge_index.shape[1] if data.edge_index.numel() > 0 else 0
    has_nan_x = bool(torch.isnan(data.x).any() or torch.isinf(data.x).any())
    has_nan_edges = (
        bool(torch.isnan(data.edge_weight).any() or torch.isinf(data.edge_weight).any())
        if data.edge_weight is not None and data.edge_weight.numel() > 0 else False
    )
    connected  = set(data.edge_index.flatten().tolist()) if E > 0 else set()
    n_isolated = N - len(connected)
    return {
        "zero_edges":   E == 0,
        "single_node":  N == 1,
        "has_nan_x":    has_nan_x,
        "has_nan_edges":has_nan_edges,
        "has_isolated": n_isolated > 0,
        "n_isolated":   n_isolated,
        "n_nodes": N, "n_edges": E,
    }

# ─── Graph repair ─────────────────────────────────────────────────────────────
def repair_graph(data, flags):
    x  = data.x.clone()
    x[torch.isnan(x) | torch.isinf(x)] = 0.0

    ew = data.edge_weight.clone() if data.edge_weight is not None else \
         torch.ones(data.edge_index.shape[1], dtype=torch.float32)
    ew[torch.isnan(ew) | torch.isinf(ew)] = 1.0

    edge_index = data.edge_index
    N = data.num_nodes

    if flags["zero_edges"] or flags["single_node"]:
        data.x = x;  data.edge_weight = ew;  data.degenerate = True
        return data

    if flags["has_isolated"]:
        loop = torch.arange(N, dtype=torch.long)
        edge_index = torch.cat([edge_index, torch.stack([loop, loop])], dim=1)
        ew         = torch.cat([ew, torch.ones(N, dtype=torch.float32)])

    data.x = x;  data.edge_index = edge_index;  data.edge_weight = ew
    data.degenerate = False
    return data

# ─── Build one graph ──────────────────────────────────────────────────────────
def build_graph(nodes, label):
    N = nodes.shape[0]
    src, dst, weights = [], [], []
    for i in range(N - 1):
        j = i + 1;  w = cosine_sim(nodes[i], nodes[j])
        src += [i, j];  dst += [j, i];  weights += [w, w]

    data = Data(
        x           = torch.from_numpy(nodes).float(),
        edge_index  = torch.tensor([src, dst], dtype=torch.long),
        edge_weight = torch.tensor(weights, dtype=torch.float32),
        y           = torch.tensor([label], dtype=torch.float32),
    )
    return repair_graph(data, validate_graph(data))

