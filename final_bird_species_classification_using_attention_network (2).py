class FreqMask(nn.Module):
    """Mask up to F consecutive mel bins (applied per sample)."""
    def __init__(self, F: int = 27):
        super().__init__()
        self.F = F

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        # x: (batch, n_mels, n_frames)
        b, m, t = x.shape
        f = random.randint(0, self.F)
        f0 = random.randint(0, max(0, m - f))
        x[:, f0 : f0 + f, :] = 0.0
        return x

class TimeMask(nn.Module):
    """Mask up to T consecutive time frames."""
    def __init__(self, T: int = 100):
        super().__init__()
        self.T = T

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        b, m, t = x.shape
        tau = random.randint(0, min(self.T, t))
        t0  = random.randint(0, max(0, t - tau))
        x[:, :, t0 : t0 + tau] = 0.0
        return x

class TimeWarp(nn.Module):
    """
    Lightweight time-warp: randomly stretch or compress the left half
    of the spectrogram and squeeze the right half to compensate.
    Implemented via grid_sample for differentiable warp.
    """
    def __init__(self, W: int = 20):
        super().__init__()
        self.W = W

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        if self.W == 0:
            return x
        b, m, t = x.shape
        # warp pivot
        center = t // 2
        w = random.randint(0, self.W)
        warped_center = center + w

        # Build normalized source coordinates for grid_sample
        # grid_sample expects (b, h_out, w_out, 2) in range [-1, 1]
        theta = torch.zeros(b, 2, 3, device=x.device)
        theta[:, 0, 0] = 1.0
        theta[:, 1, 1] = 1.0
        grid = F.affine_grid(theta, (b, 1, m, t), align_corners=True)  # (b, m, t, 2)

        # Scale x-coordinates piecewise
        norm_center  = 2.0 * center         / (t - 1) - 1.0
        norm_warped  = 2.0 * warped_center  / (t - 1) - 1.0

        grid_x = grid[..., 0]   # (b, m, t)
        left   = grid_x <= norm_warped
        right  = ~left

        if norm_warped != -1.0:
            scale_l = (norm_center + 1.0) / (norm_warped + 1.0 + 1e-6)
            grid_x[left]  = (grid_x[left] + 1.0) * scale_l - 1.0
        if norm_warped != 1.0:
            scale_r = (1.0 - norm_center) / (1.0 - norm_warped + 1e-6)
            grid_x[right] = 1.0 - (1.0 - grid_x[right]) * scale_r

        grid[..., 0] = grid_x
        x_4d   = x.unsqueeze(1)                          # (b, 1, m, t)
        warped = F.grid_sample(x_4d, grid, align_corners=True, mode="bilinear")
        return warped.squeeze(1)                          # (b, m, t)

class SpecAugment(nn.Module):
    """Combines FreqMask + TimeMask + TimeWarp. Only call during training."""
    def __init__(self, F: int = 27, T: int = 100, W: int = 20):
        super().__init__()
        self.freq_mask = FreqMask(F)
        self.time_mask = TimeMask(T)
        self.time_warp = TimeWarp(W)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        x = self.time_warp(x)
        x = self.freq_mask(x)
        x = self.time_mask(x)
        return x

