# 2.  PER-CLASS THRESHOLD TUNING
# ═══════════════════════════════════════════════════════════════════════════════

def tune_thresholds(
    y_true_val: np.ndarray,
    y_prob_val: np.ndarray,
    search_grid: np.ndarray | None = None,
) -> np.ndarray:
    """
    Optimise per-class sigmoid threshold on the validation set to maximise
    macro F1. Searches a grid of candidate thresholds independently per class.

    Returns
    -------
    thresholds : (NUM_CLASSES,) float array — one optimal threshold per class
    """
    if search_grid is None:
        search_grid = np.arange(0.1, 0.95, 0.05)

    best_thresholds = np.full(NUM_CLASSES, 0.5)

    print("\nTuning per-class thresholds on validation set...")
    for c in range(NUM_CLASSES):
        best_f1  = -1.0
        best_thr = 0.5
        for thr in search_grid:
            pred_c = (y_prob_val[:, c] >= thr).astype(int)
            f1_c   = f1_score(y_true_val[:, c], pred_c, zero_division=0)
            if f1_c > best_f1:
                best_f1  = f1_c
                best_thr = thr
        best_thresholds[c] = best_thr
        print(f"  [{c:>2}] {CLASS_NAMES[c]:<25} thr={best_thr:.2f}  val_f1={best_f1:.4f}")

    np.save(os.path.join(OUT_DIR, "optimal_thresholds.npy"), best_thresholds)
    print(f"\n  Thresholds saved → {OUT_DIR}/optimal_thresholds.npy")
    return best_thresholds


