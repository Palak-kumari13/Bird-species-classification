def plot_roc_curves(y_true: np.ndarray, y_prob: np.ndarray, tag: str = "test"):
    """
    Plot per-class ROC curves on a single axes, plus macro-average.
    Saves roc_curves_<tag>.png to OUT_DIR.
    """
    fig, ax = plt.subplots(figsize=(10, 8))
    colors  = plt.cm.get_cmap(CMAP)(np.linspace(0, 1, NUM_CLASSES))

    aucs = []
    for c, color in zip(range(NUM_CLASSES), colors):
        if y_true[:, c].sum() == 0:
            continue
        fpr, tpr, _ = roc_curve(y_true[:, c], y_prob[:, c])
        roc_auc     = auc(fpr, tpr)
        aucs.append(roc_auc)
        ax.plot(fpr, tpr, color=color, lw=1.5,
                label=f"{CLASS_NAMES[c]}  (AUC={roc_auc:.3f})")

    ax.plot([0, 1], [0, 1], "k--", lw=1, label="Random")
    ax.set_xlabel("False Positive Rate", fontsize=12)
    ax.set_ylabel("True Positive Rate",  fontsize=12)
    ax.set_title(f"ROC Curves — per class [{tag}]\nMacro AUC = {np.mean(aucs):.4f}",
                 fontsize=13, fontweight="bold")
    ax.legend(loc="lower right", fontsize=8)

    plt.tight_layout()
    out_path = os.path.join(OUT_DIR, f"roc_curves_{tag}.png")
    plt.savefig(out_path, dpi=150, bbox_inches="tight")
    plt.show()
    print(f"  Saved → {out_path}")


def plot_spectrogram_with_predictions(
    wav_path: str,
    window_probs: np.ndarray,
    thresholds: np.ndarray,
    stride_sec: float = STRIDE_HALF / TARGET_SR,
):
    """
    Display log-mel spectrogram of a recording with per-class prediction
    overlaid as a colour-coded strip below the spectrogram.

    Parameters
    ----------
    wav_path      : path to the audio file (any duration)
    window_probs  : (T, C) sigmoid probabilities per window
    thresholds    : (C,) per-class thresholds
    stride_sec    : time advance per window
    """
    y, sr = librosa.load(wav_path, sr=TARGET_SR, mono=True)
    mel   = librosa.feature.melspectrogram(y=y, sr=sr, n_fft=1024,
                                           hop_length=320, n_mels=64,
                                           fmin=50, fmax=14000)
    log_mel = librosa.power_to_db(mel, ref=np.max)

    T, C     = window_probs.shape
    duration = len(y) / sr

    fig, (ax_spec, ax_pred) = plt.subplots(
        2, 1, figsize=(14, 6),
        gridspec_kw={"height_ratios": [3, 1]},
    )
    fig.suptitle(f"Spectrogram + Predictions\n{Path(wav_path).name}",
                 fontsize=12, fontweight="bold")

    # Spectrogram
    librosa.display.specshow(log_mel, sr=sr, hop_length=320,
                             x_axis="time", y_axis="mel",
                             fmin=50, fmax=14000, ax=ax_spec, cmap="magma")
    ax_spec.set_title("Log-Mel Spectrogram")
    ax_spec.set_ylabel("Frequency (Hz)")

    # Prediction strip: one row per class, columns = time windows
    pred_binary = (window_probs >= thresholds).astype(float).T   # (C, T)
    ax_pred.imshow(pred_binary, aspect="auto", origin="upper",
                   extent=[0, duration, NUM_CLASSES - 0.5, -0.5],
                   cmap="RdYlGn", vmin=0, vmax=1, interpolation="nearest")
    ax_pred.set_yticks(range(NUM_CLASSES))
    ax_pred.set_yticklabels(CLASS_NAMES, fontsize=7)
    ax_pred.set_xlabel("Time (s)")
    ax_pred.set_title("Predicted Species Activity (green = present)")

    plt.tight_layout()
    out_path = os.path.join(OUT_DIR,
                            f"spec_predictions_{Path(wav_path).stem}.png")
    plt.savefig(out_path, dpi=150, bbox_inches="tight")
    plt.show()
    print(f"  Saved → {out_path}")


def plot_activity_heatmap(
    window_probs: np.ndarray,
    stride_sec: float = STRIDE_HALF / TARGET_SR,
    title: str = "Activity Heatmap",
):
    """
    Plot a time × species heatmap of model confidence (probability).
    Shows the raw probability values (not binarised) as a colour gradient.

    Parameters
    ----------
    window_probs : (T, C) float — sigmoid probabilities
    stride_sec   : seconds per window step
    title        : plot title
    """
    T, C  = window_probs.shape
    times = np.arange(T) * stride_sec

    fig, ax = plt.subplots(figsize=(max(10, T // 4), 5))
    im = ax.imshow(window_probs.T, aspect="auto", origin="upper",
                   extent=[0, times[-1], NUM_CLASSES - 0.5, -0.5],
                   cmap="YlOrRd", vmin=0, vmax=1, interpolation="bilinear")

    ax.set_yticks(range(NUM_CLASSES))
    ax.set_yticklabels(CLASS_NAMES, fontsize=8)
    ax.set_xlabel("Time (s)", fontsize=11)
    ax.set_title(title, fontsize=12, fontweight="bold")
    plt.colorbar(im, ax=ax, label="Probability")

    plt.tight_layout()
    out_path = os.path.join(OUT_DIR, "activity_heatmap.png")
    plt.savefig(out_path, dpi=150, bbox_inches="tight")
    plt.show()
    print(f"  Saved → {out_path}")


