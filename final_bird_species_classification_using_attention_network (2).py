class ConvBlock(nn.Module):
    def __init__(self, in_ch: int, out_ch: int):
        super().__init__()
        self.conv1 = nn.Conv2d(in_ch, out_ch, 3, padding=1, bias=False)
        self.conv2 = nn.Conv2d(out_ch, out_ch, 3, padding=1, bias=False)
        self.bn1   = nn.BatchNorm2d(out_ch)
        self.bn2   = nn.BatchNorm2d(out_ch)

    def forward(self, x: torch.Tensor, pool: tuple = (2, 2)) -> torch.Tensor:
        x = F.relu_(self.bn1(self.conv1(x)))
        x = F.relu_(self.bn2(self.conv2(x)))
        if pool != (1, 1):
            x = F.avg_pool2d(x, pool)
        return x

class CNN14(nn.Module):
    """
    PANNs CNN14.
    Input  : (batch, n_mels, time_frames)  — log-mel, values in [0,1]
    Output : (batch, num_classes)          — raw logits (no sigmoid)
    """
    def __init__(self, num_classes: int = NUM_CLASSES, dropout: float = 0.2):
        super().__init__()
        self.bn0 = nn.BatchNorm2d(64)

        self.block1 = ConvBlock(1,    64)
        self.block2 = ConvBlock(64,   128)
        self.block3 = ConvBlock(128,  256)
        self.block4 = ConvBlock(256,  512)
        self.block5 = ConvBlock(512,  1024)
        self.block6 = ConvBlock(1024, 2048)

        self.dropout = nn.Dropout(dropout)
        self.fc1     = nn.Linear(2048, 2048, bias=True)
        self.fc_out  = nn.Linear(2048, num_classes, bias=True)

        self._init_weights()

    def _init_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                nn.init.kaiming_normal_(m.weight, mode="fan_out", nonlinearity="relu")
            elif isinstance(m, nn.BatchNorm2d):
                nn.init.ones_(m.weight)
                nn.init.zeros_(m.bias)
            elif isinstance(m, nn.Linear):
                nn.init.xavier_uniform_(m.weight)
                nn.init.zeros_(m.bias)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        # x: (batch, n_mels, time_frames) → treat as (batch, 1, time, mel)
        x = x.unsqueeze(1)                     # (b, 1, mel, time)
        x = x.transpose(2, 3)                  # (b, 1, time, mel)

        # Input BN over mel dimension: operate on (b, mel, 1, time) temporarily
        x = x.transpose(1, 3)                  # (b, mel, time, 1)
        x = self.bn0(x)
        x = x.transpose(1, 3)                  # (b, 1, time, mel)

        x = self.block1(x, pool=(2, 2))
        x = self.dropout(x)
        x = self.block2(x, pool=(2, 2))
        x = self.dropout(x)
        x = self.block3(x, pool=(2, 2))
        x = self.dropout(x)
        x = self.block4(x, pool=(2, 2))
        x = self.dropout(x)
        x = self.block5(x, pool=(2, 2))
        x = self.dropout(x)
        x = self.block6(x, pool=(1, 1))        # no spatial pool on last block
        x = self.dropout(x)

        # Global pooling: avg + max, sum
        x_avg = x.mean(dim=[2, 3])             # (b, 2048)
        x_max = x.amax(dim=[2, 3])             # (b, 2048)
        x     = x_avg + x_max                  # (b, 2048)

        x = self.dropout(x)
        x = F.relu_(self.fc1(x))
        x = self.dropout(x)
        return self.fc_out(x)                   # (b, num_classes) — raw logits

# ─── Metrics ──────────────────────────────────────────────────────────────────
def macro_f1(logits: torch.Tensor, targets: torch.Tensor, threshold: float = 0.5) -> float:
    probs  = torch.sigmoid(logits).cpu().numpy()
    tgts   = targets.cpu().numpy()
    preds  = (probs >= threshold).astype(int)
    return f1_score(tgts, preds, average="macro", zero_division=0)

