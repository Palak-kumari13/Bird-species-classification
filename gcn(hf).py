def load_clip(path):
    clip = np.load(path).astype(np.float32)
    if len(clip) < CLIP_SAMPLES:
        clip = np.pad(clip, (0, CLIP_SAMPLES - len(clip)))
    return clip[:CLIP_SAMPLES]

def build_composite(window_plan):
    used = {sp: [] for sp in set(window_plan)}
    windows = []
    for cid in window_plan:
        pool      = clip_pool[cid]
        available = [p for p in pool if p not in used[cid]] or pool
        chosen    = rng.choice(available)
        used[cid].append(chosen)
        windows.append(load_clip(chosen))
    composite = np.concatenate(windows, axis=0)
    peak = np.abs(composite).max()
    if peak > 1e-8:
        composite = composite * (PEAK_NORM / peak)
    return composite

