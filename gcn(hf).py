class MelDataset(Dataset):
    def __init__(self, df: pd.DataFrame, apply_augment: bool = False):
        self.df            = df.reset_index(drop=True)
        self.apply_augment = apply_augment
    def __len__(self): return len(self.df)
    def __getitem__(self, idx):
        row   = self.df.iloc[idx]
        mel   = np.load(row["mel_path"]).astype(np.float32)
        label = (np.array(ast.literal_eval(row["label_onehot"]), dtype=np.float32)
                 if "label_onehot" in row
                 else class_to_onehot(int(row["class_id"])))
        if self.apply_augment:
            mel = specaugment_np(mel)
        return torch.from_numpy(mel), torch.from_numpy(label)

