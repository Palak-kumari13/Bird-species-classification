def fetch_species(genus, sp, en_name, code):
    records  = []
    rejected = 0
    page     = 1
    while True:
        params = {"query": f"gen:{genus} sp:{sp}", "page": page, "key": API_KEY}
        try:
            resp = requests.get(BASE_URL, params=params, timeout=30)
            if not resp.ok:
                print(f"    HTTP {resp.status_code}"); break
            data      = resp.json()
            num_pages = int(data.get("numPages", 1))
            recs      = data.get("recordings", [])
            valid, dropped = [], 0
            for r in recs:
                dur = parse_duration_secs(r.get("length", ""))
                if dur > 0 and (dur < MIN_DURATION_SEC or dur > MAX_DURATION_SEC):
                    dropped += 1
                    continue
                r["_code"] = code
                r["_en"]   = en_name
                valid.append(r)
            records.extend(valid)
            rejected += dropped
            print(f"    page {page}/{num_pages}  fetched {len(recs)}  "
                  f"kept {len(valid)}  rejected {dropped}  "
                  f"(total available: {data.get('numRecordings', 0)})")
            if page >= num_pages: break
            page += 1
            time.sleep(0.4)
        except Exception as e:
            print(f"    page {page} error: {e}"); break
    print(f"  total rejected [{code}]: {rejected}  valid: {len(records)}")
    return records

