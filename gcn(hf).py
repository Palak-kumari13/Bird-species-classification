def run_val(model, loader, thr=0.5):
    model.eval()
    all_probs, all_labels, total_loss = [], [], 0.
    with torch.no_grad():
        for X, y in loader:
            X, y    = X.to(DEVICE), y.to(DEVICE)
            probs   = model(X)
            total_loss += criterion(probs, y).item() * len(X)
            all_probs.append(probs.cpu().numpy())
            all_labels.append(y.cpu().numpy())
    probs_np  = np.vstack(all_probs)
    labels_np = np.vstack(all_labels).astype(int)
    avg_loss  = total_loss / len(loader.dataset)
    macro_f1  = f1_score(labels_np, (probs_np >= thr).astype(int),
                         average="macro", zero_division=0)
    return avg_loss, macro_f1, probs_np, labels_np

