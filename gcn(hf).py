class DoubleConvBlock(nn.Module):
    def __init__(self, in_ch, out_ch):
        super().__init__()
        self.block = nn.Sequential(
            nn.Conv2d(in_ch,  out_ch, 3, padding=1, bias=False),
            nn.BatchNorm2d(out_ch), nn.ReLU(inplace=True),
            nn.Conv2d(out_ch, out_ch, 3, padding=1, bias=False),
            nn.BatchNorm2d(out_ch), nn.ReLU(inplace=True),
            nn.MaxPool2d(2, 2),
        )
    def forward(self, x):
        return self.block(x)

class DeepCNN(nn.Module):
    def __init__(self, num_classes=NUM_CLASSES, dropout=0.5):
        super().__init__()
        self.features = nn.Sequential(
            DoubleConvBlock(  3,  32),
            DoubleConvBlock( 32,  64),
            DoubleConvBlock( 64, 128),
            DoubleConvBlock(128, 256),
        )
        with torch.no_grad():
            dummy    = torch.zeros(1, 3, 432, 1008)
            flat_dim = self.features(dummy).view(1, -1).shape[1]
        print(f"  DeepCNN flatten dim : {flat_dim}  (256 × {flat_dim//256} spatial pixels)")
        self.classifier = nn.Sequential(
            nn.Flatten(),
            nn.Linear(flat_dim, 512),
            nn.ReLU(inplace=True),
            nn.Dropout(dropout),
            nn.Linear(512, num_classes),
            nn.Sigmoid(),
        )
    def forward(self, x):
        return self.classifier(self.features(x))

