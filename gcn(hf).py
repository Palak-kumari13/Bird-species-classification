def per_class_report(probs, labels, thresholds, name=""):
    preds = np.stack([(probs[:,c] >= thresholds[c]).astype(int)
                      for c in range(NUM_CLASSES)], axis=1)
    rows  = []
    for c, cname in enumerate(CLASS_NAMES):
        p = precision_score(labels[:,c], preds[:,c], zero_division=0)
        r = recall_score   (labels[:,c], preds[:,c], zero_division=0)
        f = f1_score       (labels[:,c], preds[:,c], zero_division=0)
        rows.append({"class": cname, "threshold": thresholds[c],
                     "precision": p, "recall": r, "f1": f})
    df    = pd.DataFrame(rows)
    macro = df[["precision","recall","f1"]].mean()
    df.loc[len(df)] = {"class":"MACRO", "threshold": float("nan"),
                       "precision": macro["precision"],
                       "recall":    macro["recall"],
                       "f1":        macro["f1"]}
    return df

