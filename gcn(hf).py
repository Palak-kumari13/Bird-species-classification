def _time_warp(spec2d, W=80):
    H, T = spec2d.shape
    if W == 0 or T <= 2*W+1: return spec2d
    c  = np.random.randint(W, T-W)
    d  = np.random.randint(-W, W+1)
    ds = np.clip(c+d, W, T-W-1)
    fn = interp1d([0.,c,T-1.],[0.,ds,T-1.], kind="linear",fill_value="extrapolate")
    ot = np.clip(fn(np.arange(T,dtype=float)), 0., T-1.)
    ri = np.tile(np.arange(H)[:,None],(1,T)).astype(float)
    ci = np.tile(ot[None,:],(H,1))
    return map_coordinates(spec2d,[ri,ci],order=1,mode="nearest")

def specaugment_np(t, W=80, F=27, T=100, mF=1, mT=1):
    aug = t.copy(); H, Tw = aug.shape[1], aug.shape[2]
    w   = _time_warp(aug[0], W)
    aug[:] = np.stack([w, w.copy(), w.copy()], axis=0)
    for _ in range(mF):
        f=np.random.randint(0,F+1); f0=np.random.randint(0,max(1,H-f))
        aug[:,f0:f0+f,:] = 0.
    for _ in range(mT):
        t_=np.random.randint(0,T+1); t0=np.random.randint(0,max(1,Tw-t_))
        aug[:,:,t0:t0+t_] = 0.
    return aug

